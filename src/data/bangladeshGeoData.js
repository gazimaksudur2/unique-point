// Helper functions using hardcoded Bangladesh geographic data

export const getDivisions = () => {
	return bangladeshData.divisions.map((division) => ({
		value: division,
		label: division,
	}));
};

export const getDistrictsByDivision = (divisionName) => {
	const districts = bangladeshData.districts[divisionName] || [];
	return districts.map((district) => ({
		value: district,
		label: district,
	}));
};

export const getUpazilasByDistrict = (districtName) => {
	const upazilas = bangladeshData.upazilas[districtName] || [];
	return upazilas.map((upazila) => ({
		value: upazila,
		label: upazila,
	}));
};

export const getLocationNames = (divisionName, districtName, upazilaName) => {
	return {
		division: divisionName,
		district: districtName,
		upazila: upazilaName,
	};
};
